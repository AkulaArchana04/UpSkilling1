public class TypeCasting {
    public static void main(String[] args) {
        double d=5000.00;
        int res1=(int)d;
        int num=200;
        double res2=(double)num;

        System.out.println("The value after converting the double to integer is: "+res1);
        System.out.println("The value after converting Integer to double: "+res2);
    }
}
