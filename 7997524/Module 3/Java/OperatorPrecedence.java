public class OperatorPrecedence {
    public static void main(String[] args) {
        int result1=10+5*2;
        int result2=(10+5)*2;
        int result3=20/4+3*2;

        System.out.println("The result of 10+5*2 is: "+result1);
        System.out.println("The result of (10+5)*2 is: "+result2);
        System.out.println("The result of 20/4+3*2 is: "+result3);
    }
}
